#import "UIColor+SAHelpers.h"

@implementation UIColor (SAHelpers)

+ (UIColor *)SADarkGreyColor
{
    return [UIColor colorWithRed:53.0f / 255.0f
                           green:53.0f / 255.0f
                            blue:52.0f / 255.0f
                           alpha:1.0f];
}

+ (UIColor *)SALightGreyColor
{
    return [UIColor colorWithRed:227.0f / 255.0f
                           green:227.0f / 255.0f
                            blue:221.0f / 255.0f
                           alpha:1.0f];
}

@end
